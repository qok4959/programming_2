def cube_area(a):
    return 6 * a ** 2

def cube_volume(a):
    return a ** 3

def sphere_area(r):
    return 4 * 3.14 * r ** 2

def sphere_volume(r):
    return 4 / 3 * 3.14 * r ** 3

def cuboid_area(a, b, c):
    return 2 * (a * b + a * c + b * c)

def cuboid_volume(a, b, c):
    return a * b * c